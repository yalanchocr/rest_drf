def create_post(request):
    if request.method == 'POST':
        form = PostForm(request.POST)
        if form.is_valid():
            form.save()
            return redirect('post_list')  # Redirect to post list after saving
    else:
        form = PostForm()
    return render(request, 'create_post.html', {'form': form})
